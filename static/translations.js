// Translation helper function
function t(key) {
    const lang = (typeof currentLang !== 'undefined' ? currentLang : null)
        || (typeof localStorage !== 'undefined' && localStorage.getItem('soldia_lang'))
        || 'en';
    return (translations[lang] && translations[lang][key] !== undefined)
        ? translations[lang][key]
        : (translations['en'][key] !== undefined ? translations['en'][key] : key);
}

function applyTranslations(lang) {
    currentLang = lang;
    if (typeof localStorage !== 'undefined') localStorage.setItem('soldia_lang', lang);

    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        const text = t(key);
        if (text !== undefined) el.innerHTML = text;
    });

    // RTL support for Arabic and Persian
    const rtlLangs = ['ar', 'fa'];
    document.documentElement.dir = rtlLangs.includes(lang) ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
}

function initLanguage() {
    const saved = (typeof localStorage !== 'undefined' && localStorage.getItem('soldia_lang')) || 'en';
    applyTranslations(saved);
}
